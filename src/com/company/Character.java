package com.company;

public class Character {
    int name;
    int damage;
    int hp;
    int initiative;

    public int getName() {
        return this.name;
    }

    public int getDamage() {
        return this.damage;
    }

    public int getHp() {
        return this.hp;
    }

    public int getInitiative() {
        return this.initiative;
    }
}